$(function(){
	//判断用户是否登陆。

	// 动画效果 CSS3
	$('body').on('inview', '[data-animation]', function(){
		var $this = $(this);

		var animations = $this.data('animation');
		// 去掉所有空格
		animations = animations.replace(/\s+/g, '');
		// 拆分为数组
		animations = animations.split(',');
		// 添加首元素
		animations.unshift('animation');
		// 合并为字符串 "animation-animation1-animation2-..."
		animations = animations.join('-');

		var percent = $this.data('percent');

		$this.addClass(animations).css('width', percent);
	});

	// number animate
	var number_steps = {
		'money'   : $.animateNumber.numberStepFactories.separator(','),
		'time'    :
			function(now, tween){
				var m = Math.floor( now / 60 ),
					s = Math.floor( now % 60 ),
					target = $(tween.elem),
					text = '';
				if( m > 0 ){
					text += m + "'" + s;
				}else{
					text = s;
				}
				target.text(text);
			}
	};
	$('body').on('inview', '[data-animation-number]', function(){
		var $this = $(this);

		var number = $this.data('animation-number'),
			type = $this.data('type') || 'default',
			options = {
				number: number,
				easing: 'easeInQuad'
			};

		if( number_steps[type] ){
			options['numberStep'] = number_steps[type];
		}

		$this.animateNumber(options);
	});

		var url ="http://kefu.qycn.com/vclient/chat/?websiteid=121683";

