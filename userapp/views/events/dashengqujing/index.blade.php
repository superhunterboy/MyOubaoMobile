<script>
$(function(){
	var $dialog = $('#result').fadeOut(0),
		$joinBtn = $('#join'),
		$tempCont = $('#J-temp-cont'),
		$overlay = $('.overlay').fadeOut(0),
		ajaxurl = '{{route("user-activity-user-tasks.sign-task", 9)}}',
		handled;
	
	/*
	{
		msgType: 'error', // 出错（系统等）
		msg: '由于网络故障导致本次报名失败，请刷新页面后重试。'
	}
	{
		msgType: 
			'success'       // 成功
			'unrecharged'   // 未充值
		}
	}
	*/
	handled = function(resp){		
		if( !resp ) return false;
		var $template = $('.J-temp-' + resp.msgType);
		if( $template.length ){
			$tempCont.html($template.html());
			if( resp.msgType == 'error' ){
				$tempCont.find('[data-msg]').html(resp.msg);
			}
		}else{
			$template = $('.J-temp-error');
			$tempCont.html($template.html());
			$tempCont.find('[data-msg]').html('由于网络故障导致本次报名失败，请刷新页面后重试。');
		}
		$overlay.fadeIn();
		$dialog.fadeIn();
	}

	// 关闭弹窗
	$('.dialog').on('click', '[data-action="close"]', function(e){
		e.preventDefault();
		$overlay.fadeOut();
		$(this).parents('.dialog:eq(0)').fadeOut();
	});

	// 参加活动
	$joinBtn.on('click', function(e){
		e.preventDefault();
		// handled({msgType:'repeatjoin'});
		$.ajax({
			type: 'GET',
			url:  ajaxurl,
			success: function(resp){
				handled(resp);
			}
		});
	});
});
